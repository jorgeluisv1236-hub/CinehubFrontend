export function applyDisguise(key) {
  const d = DISGUISES.find(x => x.key === key) || DISGUISES[0];
  document.title = d.title;

  const svgBlob = new Blob([d.icon], { type: 'image/svg+xml' });
  const url = URL.createObjectURL(svgBlob);

  let link = document.querySelector("link[rel~='icon']");
  if (!link) {
    link = document.createElement('link');
    link.rel = 'icon';
    document.head.appendChild(link);
  }

  const old = link.href;
  link.href = url;
  if (old?.startsWith('blob:')) URL.revokeObjectURL(old);

  localStorage.setItem('cinehub_disguise', key);
}
